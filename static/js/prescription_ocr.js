// MediMatch Prescription PDF Generator
// Uses jsPDF and autoTable to generate professional prescription reports

document.addEventListener('DOMContentLoaded', () => {
    const downloadBtn = document.getElementById('downloadPdfBtn');

    if (downloadBtn) {
        downloadBtn.addEventListener('click', generatePDF);
    }
});

function generatePDF() {
    // Check if libraries are loaded
    if (!window.jspdf || !window.jspdf.jsPDF) {
        alert('PDF Generation library not loaded yet. Please wait.');
        console.error('jsPDF not found');
        return;
    }

    const { jsPDF } = window.jspdf;
    const doc = new jsPDF();

    // Brand Colors
    const primaryColor = [0, 150, 136]; // Teal #009688

    // Header
    doc.setFillColor(...primaryColor);
    doc.rect(0, 0, 210, 40, 'F');

    doc.setTextColor(255, 255, 255);
    doc.setFontSize(22);
    doc.setFont('helvetica', 'bold');
    doc.text('MediMatch', 15, 20);

    doc.setFontSize(12);
    doc.setFont('helvetica', 'normal');
    doc.text('AI-Powered Prescription Digitization', 15, 30);

    // Metadata
    doc.setTextColor(60, 60, 60);
    doc.setFontSize(10);
    const dateStr = new Date().toLocaleDateString() + ' ' + new Date().toLocaleTimeString();
    doc.text(`Generated: ${dateStr}`, 140, 30);

    // Patient/Prescription Info Section
    let yPos = 55;

    doc.setTextColor(0, 0, 0);
    doc.setFontSize(14);
    doc.setFont('helvetica', 'bold');
    doc.text('Prescription Report', 15, yPos);

    yPos += 10;
    doc.setFontSize(10);
    doc.setFont('helvetica', 'normal');

    // Use stored result if available
    const result = window.lastOCRResult || {};
    const confidence = result.overall_confidence ? (result.overall_confidence * 100).toFixed(0) + '%' : 'N/A';

    doc.text(`AI Confidence: ${confidence}`, 15, yPos);
    doc.text(`Source: ${result.source === 'hosted_api' ? 'MediMatch Cloud API' : 'Local Vision API'}`, 15, yPos + 6);

    yPos += 20;

    // Table Data Preparation
    const items = result.prescription_items || [];
    const tableData = items.map((item, index) => [
        index + 1,
        item.drug_name || 'Unknown',
        item.dosage || '-',
        item.frequency || '-',
        item.duration || '-',
        formatInstructions(item.instructions)
    ]);

    // Generate Table
    if (items.length > 0) {
        doc.autoTable({
            startY: yPos,
            head: [['#', 'Medicine', 'Dosage', 'Frequency', 'Duration', 'Instructions']],
            body: tableData,
            theme: 'grid',
            headStyles: { fillColor: primaryColor, textColor: 255 },
            columnStyles: {
                0: { cellWidth: 10 },
                1: { cellWidth: 40, fontStyle: 'bold' },
                5: { cellWidth: 'auto' }
            },
            styles: { fontSize: 9, cellPadding: 3 }
        });

        yPos = doc.lastAutoTable.finalY + 15;
    } else {
        doc.text('No medicines detected in this prescription.', 15, yPos);
        yPos += 10;
    }

    // Extracted Text Section (Optional)
    if (result.stages) {
        doc.setFontSize(12);
        doc.setFont('helvetica', 'bold');
        doc.text('Original Extracted Text', 15, yPos);
        yPos += 8;

        doc.setFontSize(9);
        doc.setFont('helvetica', 'normal');
        doc.setTextColor(80, 80, 80);

        const rawText = result.stages.correction?.corrected_text || result.stages.ocr?.raw_text || '';
        const splitText = doc.splitTextToSize(rawText, 180);
        doc.text(splitText, 15, yPos);

        yPos += (splitText.length * 5) + 15;
    }

    // Safety Warning Footer
    const pageHeight = doc.internal.pageSize.height;
    doc.setFontSize(8);
    doc.setTextColor(150, 150, 150);
    doc.text('DISCLAIMER: This report is generated by AI. Please verify with a pharmacist or doctor.', 105, pageHeight - 10, { align: 'center' });

    // Save
    doc.save('medimatch_prescription_report.pdf');
}

function formatInstructions(instr) {
    if (!instr) return '-';
    if (Array.isArray(instr)) return instr.join(', ');
    return instr;
}

/* ========================
   Prescription History
   ======================== */

async function openHistoryModal() {
    const modalEl = document.getElementById('historyModal');
    if (!modalEl) return;
    const modal = new bootstrap.Modal(modalEl);
    modal.show();
    loadHistory();
}

async function loadHistory() {
    const listDiv = document.getElementById('historyList');
    if (!listDiv) return;

    listDiv.innerHTML = '<div class="text-center p-3"><div class="spinner-border text-primary"></div><p>Loading history...</p></div>';

    try {
        const response = await fetch('/api/prescription/history');
        const data = await response.json();

        if (data.success && data.prescriptions.length > 0) {
            let html = '';
            data.prescriptions.forEach(p => {
                const date = new Date(p.upload_date).toLocaleDateString();
                const time = new Date(p.upload_date).toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' });
                const itemsCount = p.items ? p.items.length : 0;

                html += `
                <div class="col-md-6">
                    <div class="card h-100 shadow-sm border-0 user-select-none" onclick="viewPrescription(${p.id})" style="cursor: pointer; transition: transform 0.2s;">
                        <div class="card-body">
                            <div class="d-flex align-items-center mb-2">
                                <div class="bg-light rounded p-2 me-3">
                                    <i class="bi bi-file-text fs-4 text-primary"></i>
                                </div>
                                <div>
                                    <h6 class="mb-0 fw-bold text-dark">Prescription #${p.id}</h6>
                                    <small class="text-muted"><i class="bi bi-calendar2 me-1"></i>${date} at ${time}</small>
                                </div>
                            </div>
                            <div class="d-flex justify-content-between align-items-center mt-3">
                                <span class="badge bg-primary bg-opacity-10 text-primary border border-primary border-opacity-10 rounded-pill">
                                    ${itemsCount} Medications
                                </span>
                                <i class="bi bi-chevron-right text-muted"></i>
                            </div>
                        </div>
                    </div>
                </div>
                `;
            });
            listDiv.innerHTML = html;
        } else {
            listDiv.innerHTML = '<div class="text-center p-5 text-muted"><i class="bi bi-clock-history fs-1 opacity-25"></i><p class="mt-2">No upload history found.</p></div>';
        }

    } catch (e) {
        listDiv.innerHTML = '<div class="alert alert-danger">Failed to load history.</div>';
        console.error(e);
    }
}

async function viewPrescription(id) {
    // Close modal
    const modalEl = document.getElementById('historyModal');
    const modal = bootstrap.Modal.getInstance(modalEl);
    if (modal) modal.hide();

    // Show spinner in main view
    const progress = document.getElementById('progress');
    const resultsContainer = document.getElementById('resultsContainer');
    const noResults = document.getElementById('noResults');
    const errorDiv = document.getElementById('error');

    // Reset view
    resultsContainer.classList.add('d-none');
    noResults.classList.add('d-none');
    errorDiv.classList.add('d-none');

    progress.classList.remove('d-none');
    document.getElementById('progressText').textContent = "Restoring past prescription...";

    try {
        const response = await fetch(`/api/prescription/${id}`);
        const data = await response.json();

        if (data.error) throw new Error(data.error);

        // Adapt data structure to what displayResults expects
        const adaptedResult = {
            overall_confidence: data.ocr_confidence,
            source: data.api_source,
            stages: { correction: { corrected_text: data.raw_text } },
            prescription_items: data.items,
            database_id: data.id
        };

        // Show image if available
        if (data.image_path) {
            const imgPath = `/static/uploads/prescriptions/${data.image_path}`;
            const imagePreview = document.getElementById('imagePreview');
            const previewContainer = document.getElementById('previewContainer');
            const fileName = document.getElementById('fileName');

            imagePreview.src = imgPath;
            fileName.textContent = `Restored: Prescription #${data.id}`;
            previewContainer.classList.remove('d-none');
        }

        // Wait a bit for UI transition
        setTimeout(() => {
            progress.classList.add('d-none');
            // Make sure displayResults is globally available
            if (typeof displayResults === 'function') {
                displayResults(adaptedResult);
            } else {
                console.error("displayResults function not found");
                alert("Viewer error: displayResults not found");
            }
        }, 600);

    } catch (e) {
        progress.classList.add('d-none');
        const errorDiv = document.getElementById('error');
        errorDiv.classList.remove('d-none');
        document.getElementById('errorMessage').textContent = "Failed to load history item: " + e.message;
    }
}

// Ensure functions are global
window.openHistoryModal = openHistoryModal;
window.loadHistory = loadHistory;
window.viewPrescription = viewPrescription;
